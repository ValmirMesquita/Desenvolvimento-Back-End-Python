def ola_mundo(nome ):
    return f"Olá, {nome}!"
# Exemplo de uso
print(ola_mundo("Mundo"))  # Saída: Olá, Mundo! 

def soma(a, b):
    soma  = (a + b),(a - b),(a * b),(a / b )
    return soma
print(soma(3, 5))  # Saída: 8

def saudacao_personalizada(nome, saudacao):
    return f"{saudacao}, {nome}!"
# Exemplo de uso
print(saudacao_personalizada("Alice", "Bem-vinda"))  # Saída: Bem-vinda, Alice!
print(saudacao_personalizada("Mario", "Bom-dia"))  # Saída: Bom-dia, Mario!
